SELECT
	 CAB.ID_META
    ,CAB.ID_GRUPO_PAINEL
	,TGP.DESCRICAO as DESCRICAO_GRUPO
    ,CAB.ID_STATUS
	,TSP.DESCRICAO as DESCRICAO_STATUS
    ,CAB.OBSERVACAO
FROM 
	TB_CABECALHO_PAINEL_META AS CAB
INNER JOIN
	TB_STATUS_PAINEL AS TSP
	ON TSP.ID_STATUS = CAB.ID_STATUS
INNER JOIN
	TB_GRUPO_PAINEL AS TGP
	ON TGP.ID_GRUPO_PAINEL = CAB.ID_GRUPO_PAINEL
WHERE
	((@ID_META IS NULL) OR (CAB.ID_META = @ID_META))
AND
	((@ID_GRUPO_PAINEL = '00000000-0000-0000-0000-000000000000') OR (CAB.ID_GRUPO_PAINEL = @ID_GRUPO_PAINEL))
AND
	((@ID_GRUPO_PAINEL != '00000000-0000-0000-0000-000000000000') AND (CAB.ID_STATUS != @ID_STATUS_PAINEL))