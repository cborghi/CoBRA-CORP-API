SELECT
	 [ID_META_REAL]
	,[ID_USUARIO_RM]
	,[ID_LINHA_META]
	,[REALIZADO]
	,[PONDERADO]
	,[DATA_CRIACAO]
FROM
	[TB_META_REAL]
WHERE
	((@ID_USUARIO_RM = '00000000-0000-0000-0000-000000000000') OR ([ID_USUARIO_RM] = @ID_USUARIO_RM))
AND
	((@ID_LINHA_META = '00000000-0000-0000-0000-000000000000') OR ([ID_LINHA_META] = @ID_LINHA_META))
AND
	((@ID_META_REAL = '00000000-0000-0000-0000-000000000000') OR ([ID_META_REAL] = @ID_META_REAL))